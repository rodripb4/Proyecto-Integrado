class Empleado {
  constructor (id, dni, nombre, codDepartamento, departamento, codigo, cargo, email, password) {
    this.id = id
    this.nombre = nombre
    this.codDepartamento = codDepartamento
    this.departamento = departamento
    this.codigo = codigo
    this.email = email
    this.dni = dni
    this.password = password
    this.cargo = cargo
  }
}
export default Empleado
